public class Bitwise{
    public static void main(String[] args){
        System.out.println(2 & 3); //AND
        System.out.println(2 | 3); //OR
        System.out.println(2 ^ 3);  //XOR
        System.out.println(~2); //NEGATION
        System.out.println(34 << 1); //LEFT-SHIFT
        System.out.println(34 >> 2); //RIGHT-SHIFT
        System.out.println(-4 << 1); 
        System.out.println(-4 >> 1);
    }
}